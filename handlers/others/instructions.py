def show_instructions_menu(update: Update, context: CallbackContext):
    """
    –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–æ–¥–º–µ–Ω—é —Å –∫–Ω–æ–ø–∫–∞–º–∏ –¥–ª—è –∫–∞–∂–¥–æ–π –ø–ª–∞—Ç—Ñ–æ—Ä–º—ã.
    """
    query = update.callback_query
    query.answer()

    user_id = query.from_user.id
    user_id_str = str(query.from_user.id)
    client_data = get_client_by_tg_id(user_id_str)

    if not client_data:
        logger.info(f"üö´ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {user_id} –∑–∞–ø—Ä–æ—Å–∏–ª –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–∏ –±–µ–∑ –ø–æ–¥–ø–∏—Å–∫–∏")
        query.message.reply_text("üö´ –£ –≤–∞—Å –ø–æ–∫–∞ –Ω–µ—Ç –æ—Ñ–æ—Ä–º–ª–µ–Ω–Ω—ã—Ö –ø–æ–¥–ø–∏—Å–æ–∫.")
        return

    logger.info(f"üìñ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {user_id} –æ—Ç–∫—Ä—ã–ª –º–µ–Ω—é –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏–π")

    keyboard = [
        [
            InlineKeyboardButton("üñ•Ô∏è Windows", callback_data="inst_win"),
            InlineKeyboardButton("üçè MacOS", callback_data="inst_mac"),
        ],
        [
            InlineKeyboardButton("üì± iOS", callback_data="inst_ios"),
            InlineKeyboardButton("ü§ñ Android", callback_data="inst_and"),
        ],
        [InlineKeyboardButton("üîô –ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é", callback_data="back_to_main")],
    ]

    reply_markup = InlineKeyboardMarkup(keyboard)
    query.message.edit_reply_markup(reply_markup=reply_markup)


def show_instruction_text(update: Update, context: CallbackContext, platform: str):
    """
    –í—ã–≤–µ—Å—Ç–∏ –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—é –¥–ª—è –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–π –ø–ª–∞—Ç—Ñ–æ—Ä–º—ã.
    """
    user_id = update.callback_query.from_user.id
    logger.info(f"üìò –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å {user_id} –æ—Ç–∫—Ä—ã–ª –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—é –¥–ª—è {platform}")

    query = update.callback_query
    query.answer()

    text = INSTRUCTIONS.get(platform, "–ò–Ω—Å—Ç—Ä—É–∫—Ü–∏—è –Ω–µ –Ω–∞–π–¥–µ–Ω–∞.")
    if text == "–ò–Ω—Å—Ç—Ä—É–∫—Ü–∏—è –Ω–µ –Ω–∞–π–¥–µ–Ω–∞.":
        logger.warning(f"‚ö†Ô∏è –ò–Ω—Å—Ç—Ä—É–∫—Ü–∏—è –¥–ª—è {platform} –Ω–µ –Ω–∞–π–¥–µ–Ω–∞")

    keyboard = [
        [InlineKeyboardButton("üîô –ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é", callback_data="back_to_main")]
    ]
    reply_markup = InlineKeyboardMarkup(keyboard)

    query.message.edit_text(
        text,
        parse_mode="HTML",
        reply_markup=reply_markup,
    )



def show_instruction_platform_router(update: Update, context: CallbackContext):
    query = update.callback_query
    query.answer()
    data = query.data

    platform_map = {
        "inst_win": "Windows",
        "inst_mac": "MacOS",
        "inst_ios": "iOS",
        "inst_and": "Android",
    }

    platform = platform_map.get(data)
    if platform:
        show_instruction_text(update, context, platform)
